PixelSensor::PixelSensor(SpectrumPtr r, SpectrumPtr g, SpectrumPtr b, const RGBColorSpace* outputSpace, SpectrumPtr sensorIllum, float imagingRatio, Allocator alloc):r_bar(r, alloc),g_bar(g, alloc),b_bar(b, alloc),imagingRatio(imagingRatio)
{
    // Here white balance is handled by the matrix from rgb to xyz
    float rgbCamera[nSwatchReflectances][3];
    for (int i = 0; i < nSwatchReflectances; i++)
    {
        glm::vec3 rgb = project_reflectance(swatchReflectances[i], sensorIllum, &r_bar, &g_bar, &b_bar);
        for (int j = 0; j < 3; j++)
        {
            rgbCamera[i][j] = rgb[j];
        }
    }
    float xyzOutCS[nSwatchReflectances][3];
    float sensorWhiteG = inner_product(sensorIllum, &g_bar);
    float sensorWhiteY = inner_product(sensorIllum, &spec::Y());
    for (int i = 0; i < nSwatchReflectances; i++)
    {
        glm::vec3 xyz = project_reflectance(swatchReflectances[i], &outputSpace->illuminant, &spec::X(), &spec::Y(), &spec::Z()); //normalized using Y()
        xyz *= sensorWhiteY / sensorWhiteG; // Renormalize using g_bar
        for (int j = 0; j < 3; j++)
        {
            xyzOutCS[i][j] = xyz[j];
        }
    }
    math::SquareMatrix<3> tXYZFromRGB;
    if (!math::linear_least_squares(rgbCamera, xyzOutCS, nSwatchReflectances, tXYZFromRGB))
    {
        throw std::runtime_error("Failed to initialize XYZ from RGB for pixel sensor");
    }
    glm::vec3 c0 = glm::vec3(tXYZFromRGB.m[0][0], tXYZFromRGB.m[1][0], tXYZFromRGB.m[2][0]);
    glm::vec3 c1 = glm::vec3(tXYZFromRGB.m[0][1], tXYZFromRGB.m[1][1], tXYZFromRGB.m[2][1]);
    glm::vec3 c2 = glm::vec3(tXYZFromRGB.m[0][2], tXYZFromRGB.m[1][2], tXYZFromRGB.m[2][2]);
    XYZFromSensorRGB = glm::mat3(c0,c1,c2);
}

