glm::vec3 PixelSensor::project_reflectance(SpectrumPtr refl, SpectrumPtr illum, SpectrumPtr b1, SpectrumPtr b2, SpectrumPtr b3)
{
    glm::vec3 res(0.0f);
    float g_int = 0;
    for (float l = spec::gLambdaMin; l <= spec::gLambdaMax; l++)
    {
        g_int += b2(l) * illum(l);
        res.x += refl(l) * b1(l) * illum(l);
        res.y += refl(l) * b2(l) * illum(l);
        res.z += refl(l) * b3(l) * illum(l);
    }
    return res / g_int;
}

__device__ void RGBFilm::add_radiance(const glm::vec3& xyz, int pixelIndex)
{
    // add linear rgb radiance
    glm::vec3 rgb = outputSpace->to_rgb(xyz);
    float maxc = max(rgb.x, max(rgb.y, rgb.z));
    if (maxc > threshold)
    {
        rgb *= threshold / maxc;
    }
    image[pixelIndex] += rgb;
}

